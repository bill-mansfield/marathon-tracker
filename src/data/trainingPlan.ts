import type { PlanWeek } from "./types";

function week(
  weekStart: string,
  weekType: string,
  mon: [number, string],
  tue: [number, string],
  wed: [number, string],
  thu: [number, string],
  fri: [number, string],
  sat: [number, string],
  sun: [number, string],
  totalKm: number,
  notes: string,
  longRunPlan: string = ""
): PlanWeek {
  const dayNames = ["MON", "TUE", "WED", "THU", "FRI", "SAT", "SUN"] as const;
  const dayData = [mon, tue, wed, thu, fri, sat, sun];
  return {
    weekStart,
    weekType,
    days: dayNames.map((day, i) => ({
      day,
      km: dayData[i][0],
      description: dayData[i][1],
    })),
    totalKm,
    notes,
    longRunPlan,
  };
}

